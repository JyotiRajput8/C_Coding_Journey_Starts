#include <stdio.h>

/*
Header File: stdio.h

The stdio.h header file provides input and output functions in C.

Why we use stdio.h:
1. To display output on the screen
2. To take input from the user
3. To use functions like printf() and scanf()
*/

int main()
{
    /*
    main() Function

    The main() function is the starting point of every C program.
    Program execution begins from this function.

    Breakdown:
    int    → Returns an integer value
    main() → Entry point of the program
    */

    printf("Hello Visual Studio\n");
    printf("I am here to learn C programming with you.\n");
    printf("Please guide me.\n");

    /*
    printf() Function

    printf() is used to display output on the screen.

    Output:
    Hello Visual Studio
    I am here to learn C programming with you.
    Please guide me.
    */

    return 0;

    /*
    return 0;

    Indicates that the program executed successfully.
    A return value of 0 means no errors occurred.
    */
}